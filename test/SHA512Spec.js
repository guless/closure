function test_sha512( input, expect ) {
    SHA512API = new SHA512();
    SHA512API.update( ascii(input) );
    
    var result = hexof(SHA512API.final());
    assert(result == expect, "SHA512 does not match." + ` { input="${input}", expect="${expect}", result="${result}" }`);
    passlog(`"${input}"`, `"${result}"`);
}